    private void jAgregarPostreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarPostreActionPerformed
        String codigo=jCodigo.getText();
        String nombre = jNomPostre.getText();
        if(nombre.equalsIgnoreCase("") && codigo.equalsIgnoreCase(""))
        {
             JOptionPane.showMessageDialog(this,"Ingrese los datos por favor");
        }
        else
        {
        pos=new Postre(codigo,nombre ,listaIngredientes);

        listaIngredientes= new ListaLEG<Ingrediente>();

        postre.agregarPostre(pos);
        jCodigo.setText("");
        jNomPostre.setText("");
        jMostrar1ActionPerformed(evt);
        limpiar();
        }
    }//GEN-LAST:event_jAgregarPostreActionPerformed

