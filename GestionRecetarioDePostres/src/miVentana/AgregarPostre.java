    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String []titulo={"Nombre","Cantidad"};
        modelo=new DefaultTableModel(null,titulo);
        String codigo = jCodigo.getText();

        if(codigo.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this,"Ingrese un codigo por favor");
        }else {
            Postre postrecito = postre.buscarPostre(codigo);
            if(postrecito!=null){
                jNomPostre.setText(postrecito.getNombrePostre());
                Nodo<Ingrediente> aux=postrecito.getListaIngrediente().getPrimero();
                while(aux!=null){
            modelo.addRow(new String[]{aux.getDato().getNombreIngrediente(),
                String.valueOf(aux.getDato().getCantidad())});

        aux=aux.getSiguiente();
        }
        jTableIngredientes.setModel(modelo); 
                

            } else {
                JOptionPane.showMessageDialog(this,"El codigo: "+codigo+" , no esta en la lista..");
            }
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

